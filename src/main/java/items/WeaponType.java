package items;

/**
 * Enumeration representing different types of weapons.
 */
public enum WeaponType {
    /** Standard weapon type with no special advantage. */
    NORMAL,
    /** Sharp weapon type, effective against Rotten Zombies. */
    SHARP,
    /** Heavy weapon type, effective against Armored Zombies. */
    HEAVY,
    /** Ranged weapon type, effective against Runner Zombies. */
    GUN
}
